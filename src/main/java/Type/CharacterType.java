package Type;

public class CharacterType {
    private String type;
    private String size;
    //constructor
    public CharacterType() {
    }
    public String getType() {
        return this.type;
    }
    public void setType(String type){
        this.type = type;
    }
    public String getSize() {
        return this.size;
    }
    public void setSize(String size) {
        this.size = size;
    }
}
